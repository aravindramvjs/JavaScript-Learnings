// if statement = a basic form of decision making 
// if a condition is true , do this 
// if not , do this 


let time = window.prompt("what is the time now? ");

if(time >= 5 & time <=6){
    console.log("RUN")
}
else if(time <= 5){
    console.log("Sleep")
}
else if(time >= 6 & time <=8){
    console.log("Get Ready For the Office")
}
else if(time >=8 & time <= 12){
    console.log("Focus on work")
}
else{
    console.log("Enter Proper date")
}

